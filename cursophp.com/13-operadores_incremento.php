<?php
// Operadores de Incremento e Decremento
// Servem para somar +1 ou subtrair -1 em qualquer valor do tipo numérico ou  string.

$valor = 20;

// Pré-incremento ++
echo ++$valor;
echo "<hr>";

// Pré-decremento --
echo --$valor;
echo "<hr>";


// Pós-incremento ++
echo $valor++;
echo "<hr>";

// Pós-decremento --
echo $valor--;
echo "<hr>";
